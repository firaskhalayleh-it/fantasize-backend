import {
  Entity,
  PrimaryGeneratedColumn,
  Column,
  ManyToOne,
  CreateDateColumn,
  BaseEntity,
  Relation,
  OneToOne
} from 'typeorm';
import { Roles } from './Roles';

@Entity()
export class Users extends BaseEntity {
  @PrimaryGeneratedColumn('uuid')
  UserID: string;

  @ManyToOne(() => Roles, (role) => role.RoleID)
  Role: Relation<Roles>;

  @Column('varchar')
  Username: string;

  @Column('varchar')
  Email: string;

  @Column('varchar')
  Password: string;

  @Column('bytea', { nullable: true })
  UserProfilePicture: Buffer;

  @Column('varchar', { nullable: true })
  PhoneNumber: string;

  @Column('varchar', { nullable: true })
  Gender: string;

  @Column('varchar', { nullable: true })
  firebaseUID: string;

  @CreateDateColumn()
  CreatedAt: Date;

  @CreateDateColumn()
  UpdatedAt: Date;


  toJSON() {
    const { Password, ...userWithoutPassword } = this;
    return userWithoutPassword;
  }
}
